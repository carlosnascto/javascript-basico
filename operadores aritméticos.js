// OPERADORES ARITMÉTICOS
// +, -, *, /, **

let numero = 50 

console.log(numero + numero);
console.log(numero - numero);
console.log(numero * numero);
console.log(numero / numero);
console.log(3 ** 3);

// INCREMENTAÇÃO E DECREMENTAÇÃO
// ++. --

console.log(--numero);
console.log(++numero);
console.log(++numero);

console.log(numero);
